					Hispaniola is an island in the Caribbean that is part of the Greater Antilles. Hispaniola is the most populous island in the West Indies, and the region's second largest in area, after the main island of Cuba (Embassy of the Dominican Republic, in the United States, 2007).<br><br> The 76,192-square-kilometre island is divided into two separate nations: the Spanish-speaking Dominican Republic to the east and the French / Haitian Creole-speaking Haiti to the west (Embassy of the Dominican Republic, in the United States, 2007).<br><br> The only other divided island in the Caribbean is Saint Martin, which is shared between France (Saint Martin) and the Netherlands (Sint Maarten). Hispaniola is the site of one of the first European settlements in the Americas, La Navidad (1492-1493), as well as the first proper town, La Isabela (1493-1500), and the first permanent settlement - the current capital of the Dominican Republic, Santo Domingo (Central Intelligence Agency, 2021). <br><br>These settlements were founded successively in each of Christopher Columbus's first three voyages (Embassy of the Dominican Republic, in the United States, 2007).
