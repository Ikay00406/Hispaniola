					<h3>Citadelle Haiti</h3>
				</p>
				<p>
				The Citadelle is one of the most important sites in Haitian history. The Haitian revolutionary Henri Christophe, along with thousands of newly emancipated Haitian people, built this fortress on the northern coast in 1805 as part of a defense strategy against a potential French invasion. <br><br> Architecturally, the Citadelle is one of the few examples of Afro-colonial architecture in the New World and is worth visiting just for its unique architectural features and awe-inspiring size. The fortress, located on top of the Bonnet a l'Eveque mountain, also offers stunning views of Cap-Haitien and the northern coast. Visitors often combine a visit to the Citadelle with an excursion to nearby Sans-Souci Palace (globalgrasshopper, 2021).
